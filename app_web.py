def generate_cover(topic, author):

    prompt = f"Professional book cover for a book about {topic}, author: {author}"

    try:
        response = client.images.generate(
            model="gpt-image-1",
            prompt=prompt,
            size="1024x1024"
        )

        image_base64 = response.data[0].b64_json
        image_bytes = base64.b64decode(image_base64)
        image = Image.open(BytesIO(image_bytes))

        draw = ImageDraw.Draw(image)
        width, height = image.size

        draw.text((width/2 - 150, height - 120), topic.upper(), fill="white")
        draw.text((width/2 - 150, height - 60), author.upper(), fill="white")

        return image

    except Exception as e:
        st.error(f"Error generating cover: {e}")
        return None
