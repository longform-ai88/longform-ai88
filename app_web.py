def create_pdf(text, cover_image, filename="book.pdf"):
    c = canvas.Canvas(filename, pagesize=letter)
    width, height = letter
    cover = ImageReader(cover_image)
    c.drawImage(cover, 0, 0, width=width, height=height)
    c.showPage()
    c.setFont("Helvetica", 18)
    c.setFont("Helvetica", 14)
    c.drawCentredString(width/2, height - 320, "Generated by your AI tool")
    c.showPage()
    c.setFont("Helvetica-Bold", 24)
    c.drawString(200, height - 100, "Table of contents")
    c.setFont("Helvetica", 14)
    y_toc = height - 150
    for line in text.split("\n"):
        if line.startwith("chapter"):
            c.drawString(100, y_toc, line)
            y_toc -= 25
    c.showPage()
    c.setFont("Helvetica-Bold", 24)
    c.drawString(180, height - 80, "AI Generated Book")
    c.setFont("Helvetica", 12)
    y = height - 120
    page = 1
    for line in text.split("\n"):
        c.drawString(40, y, line)
        y -= 20
        if y < 40:
            c.drawString( - 100, 20, f"Page {page}")
            page +=1
            c.showPage()
            c.setFont("Helvetica", 12)
            y = height - 40
            c.save()
